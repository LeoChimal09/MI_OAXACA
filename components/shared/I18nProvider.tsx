function normalizeLookupKey(value: string) {
  return value
    .trim()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

function resolveCanonicalCategory(category: string) {
  return canonicalCategoryKeys[normalizeLookupKey(category)] ?? category;
}

