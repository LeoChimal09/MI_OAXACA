const I18nContext = createContext<I18nContextValue | undefined>(undefined);

function interpolate(template: string, params?: TranslationParams) {
  if (!params) return template;

  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");
  const [hasLoadedStoredLocale, setHasLoadedStoredLocale] = useState(false);

  useEffect(() => {
    try {
      const stored = window.localStorage.getItem(STORAGE_KEY);
      if (stored === "en" || stored === "es") {
        setLocaleState(stored);
      }
    } catch {
      // ignore storage access issues
    } finally {
      setHasLoadedStoredLocale(true);
    }
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale;

    if (!hasLoadedStoredLocale) {
      return;
    }

    try {
      window.localStorage.setItem(STORAGE_KEY, locale);
    } catch {
      // ignore storage access issues
    }
  }, [hasLoadedStoredLocale, locale]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    setLocale: setLocaleState,
    t: (key, params) => interpolate(messages[locale][key] ?? messages.en[key] ?? key, params),
    categoryLabel: (category) => {
      const canonicalCategory = resolveCanonicalCategory(category);
      return categoryLabels[locale][canonicalCategory] ?? canonicalCategory;
    },
    dayLabel: (day) => dayLabels[locale][day] ?? day,
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}